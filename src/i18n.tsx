const dictionaries = { en, az };
type Values = Record<string, string | number>;

interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, values?: Values) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => {
    const saved = window.localStorage.getItem("movemind-locale");
    return saved === "az" ? "az" : "en";
  });

  useEffect(() => {
    document.documentElement.lang = locale;
    window.localStorage.setItem("movemind-locale", locale);
  }, [locale]);

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t: (key, values = {}) =>
        Object.entries(values).reduce(
          (copy, [name, replacement]) =>
            copy.replaceAll(`{${name}}`, String(replacement)),
          dictionaries[locale][key],
        ),
    }),
    [locale],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error("useI18n must be used inside I18nProvider");
  return context;
}
